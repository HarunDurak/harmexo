// Kilit eşikleri: o ana kadar tamamlanan toplam level sayısı
// Ember(10) → Frost(10) → Storm(8) → Verdant(7) → Celestial(8)
export const REALM_UNLOCK_LEVEL = { ember: 0, frost: 10, storm: 20, verdant: 28, celestial: 35 };

export function getLevelsByRealm(realm) { return LEVELS.filter(l => l.realm === realm); }
export function getLevelById(id) { return LEVELS.find(l => l.id === id) || null; }

// Mevcut leveldan sonra gelen ilk leveli döndürür (ID sırasına göre)
export function getNextLevel(currentId) {
  const sorted = [...LEVELS].sort((a, b) => a.id - b.id);
  const idx = sorted.findIndex(l => l.id === currentId);
  return idx !== -1 && idx + 1 < sorted.length ? sorted[idx + 1] : null;
}
